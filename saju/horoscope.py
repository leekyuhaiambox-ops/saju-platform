def sign_for_date(month: int, day: int) -> str:
    """월·일 → 별자리 slug."""
    for s in SIGNS:
        slug, _, _, (sm, sd), (em, ed), _, _ = s
        if sm == 12:  # 염소자리 (12/22 ~ 1/19)
            if (month == 12 and day >= sd) or (month == 1 and day <= ed):
                return slug
        elif (month == sm and day >= sd) or (month == em and day <= ed):
            return slug
    return "capricorn"


def get_sign(slug: str):
    for s in SIGNS:
        if s[0] == slug:
            base = {"slug": s[0], "name": s[1], "name_en": s[2],
                    "start": s[3], "end": s[4], "element": s[5], "planet": s[6],
                    "date_range": f"{s[3][0]}월 {s[3][1]}일 ~ {s[4][0]}월 {s[4][1]}일"}
            base.update(DETAIL.get(s[0], {}))
            return base
    return None


