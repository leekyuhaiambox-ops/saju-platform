def interpret(saju):
    """SajuResult → 해석 dict 반환."""
    day_idx = saju.day_pillar.stem * 6 + (saju.day_pillar.branch - saju.day_pillar.stem) % 12
    # 정확한 60갑자 인덱스 산출 — 일주 stem(0..9), branch(0..11)로부터 0..59 찾기
    day_idx = _find_pillar_index(saju.day_pillar.stem, saju.day_pillar.branch)

    daypillar_info = DAY_PILLAR_INTERPRETATIONS.get(day_idx,
        ("미상", "기본 풀이", "해석 데이터 준비 중입니다."))

    # 십신 분포 카운트
    ten_god_count = {g: 0 for g in D.TEN_GODS}
    for label, val in saju.ten_gods.items():
        if val:
            ten_god_count[val] = ten_god_count.get(val, 0) + 1

    # 오행 균형 분석
    total = sum(saju.element_counts.values()) or 1
    elem_pct = {k: round(v / total * 100, 1) for k, v in saju.element_counts.items()}
    max_elem = max(saju.element_counts, key=saju.element_counts.get)
    min_elem = min(saju.element_counts, key=saju.element_counts.get)

    # 십이운성 일주 단계
    day_stage = saju.twelve_stages["day"]

    # 종합 한 줄
    daymaster_elem = saju.day_master_element
    summary_line = (
        f"{daypillar_info[0]} 일주 — {daypillar_info[1]}. "
        f"일간 {saju.day_master}({daymaster_elem})은 일지에서 「{day_stage}」 상태이며, "
        f"오행 중 {max_elem}이(가) 가장 강하고 {min_elem}이(가) 약합니다."
    )

    return {
        "day_pillar_name": daypillar_info[0],
        "day_pillar_headline": daypillar_info[1],
        "day_pillar_detail": daypillar_info[2],
        "summary_line": summary_line,
        "ten_god_counts": ten_god_count,
        "ten_god_meanings": TEN_GOD_MEANING,
        "element_pct": elem_pct,
        "max_element": max_elem,
        "min_element": min_elem,
        "element_meaning": ELEMENT_MEANING,
        "day_twelve_stage": day_stage,
        "twelve_stage_meaning": TWELVE_STAGE_MEANING,
    }


def _find_pillar_index(stem: int, branch: int) -> int:
    """천간(0..9) 지지(0..11)로부터 60갑자 인덱스 산출."""
    for i in range(60):
        if i % 10 == stem and i % 12 == branch:
            return i
    return 0
